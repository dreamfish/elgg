		}
			
	// List tasks
		$area2 = elgg_view_title(sprintf(elgg_echo('tasks:read'), $page_owner->name));
		set_context('search');
		$area2 .= list_entities('object','tasks',page_owner());
		set_context('tasks');
		
	// Format page
		$body = elgg_view_layout('two_column_left_sidebar', $area1, $area2);
		
